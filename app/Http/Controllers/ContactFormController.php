    public function store(Request $request)//Requestクラスを使ってデータを持ってくる(RequestはRequestクラスをインスタンス化したもの)＝(DI:依存性の注入)
    //Requestは5行目で生成したクラスのインスタンスであり,引数にして呼び出している
    {
        $contact = new ContactForm;// 保存するモデルのインスタンスを作成
